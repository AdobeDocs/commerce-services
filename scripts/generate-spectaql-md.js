#!/usr/bin/env node
'use strict';

const { execSync } = require('child_process');
const fs = require('fs');
const path = require('path');

const envFile = path.join(__dirname, '../.env');
if (fs.existsSync(envFile)) {
  for (const line of fs.readFileSync(envFile, 'utf8').split('\n')) {
    const match = line.match(/^\s*([A-Z_][A-Z0-9_]*)=(.*)$/);
    if (match && !(match[1] in process.env)) process.env[match[1]] = match[2].trim();
  }
}

const ROOT = path.join(__dirname, '..');
const SPECTAQL_BIN = path.join(ROOT, 'node_modules/.bin/spectaql');
const CONFIG = path.join(ROOT, 'spectaql/config-merchandising-md.yml');
const TARGET_DIR = path.join(ROOT, 'src/pages/includes/autogenerated');
const TARGET_FILE = 'merchandising-api.md';
const OUTPUT_FILE = path.join(TARGET_DIR, TARGET_FILE);

console.log('Running SpectaQL with Markdown theme...');
execSync(
  `"${SPECTAQL_BIN}" "${CONFIG}" --target-file "${TARGET_FILE}" --target-dir "${TARGET_DIR}"`,
  { stdio: 'inherit', cwd: ROOT }
);

if (!fs.existsSync(OUTPUT_FILE)) {
  console.error(`SpectaQL did not produce ${OUTPUT_FILE}`);
  process.exit(1);
}

const content = fs.readFileSync(OUTPUT_FILE, 'utf8');
const cleaned = content.replace(/\n{3,}/g, '\n\n').trimEnd() + '\n';
fs.writeFileSync(OUTPUT_FILE, cleaned, 'utf8');

for (const dir of ['javascripts', 'stylesheets']) {
  fs.rmSync(path.join(TARGET_DIR, dir), { recursive: true, force: true });
}

console.log(`Generated: ${OUTPUT_FILE}`);
